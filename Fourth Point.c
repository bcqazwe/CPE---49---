#include <stdio.h>
#include <stdlib.h>

/*
 * 輸入的兩條線段端點座標（全域變數方便存取）
 * 線段一：(x1,y1)-(x2,y2)
 * 線段二：(x3,y3)-(x4,y4)
 */
double x1, y1, x2, y2, x3, y3, x4, y4;

/*
 * 用來存放平行四邊形三個已知頂點：
 * b：兩線段共享的頂點（對角線上一端）
 * a、c：另外兩個端點
 */
double ax, ay, bx, by, cx, cy;

/*
目標：
給定兩條線段（各由兩個端點表示），這兩條線段共用一個端點，
找出使這三個點構成平行四邊形的第四個頂點座標。

解法：
1. 先找出兩條線段共用的端點 B。
2. 另外兩個端點分別為 A 與 C。
3. 平行四邊形的第四點 D = A + C - B
   （因為在平行四邊形中，對角線互相平分：中點相同，即 (A+D)/2 = (B+C)/2）

技巧：
1. 列出四種可能的共用端點組合（x1=x3、x1=x4、x2=x3、x2=x4）逐一比對。
2. 找到共用點後，套用公式 D = A + C - B 即可求出第四點。
3. 這道題的數學核心是「對角線中點相等」，只需一行公式就能解決。
*/

int main()
{
    /* 持續讀入直到 EOF，每組輸入兩條線段的四個端點 */
    while(scanf("%lf %lf %lf %lf %lf %lf %lf %lf", &x1, &y1, &x2, &y2, &x3, &y3, &x4, &y4) != EOF)
    {
        /* 找出兩條線段共用的端點 b，其餘兩個端點為 a、c */
        if (x1 == x3 && y1 == y3) 
        {
            /* 線段一的起點 = 線段二的起點 → 共用點為 (x1,y1) */
            bx = x1; by = y1;
            ax = x2; ay = y2;
            cx = x4; cy = y4;
        } else if (x1 == x4 && y1 == y4) 
        {
            /* 線段一的起點 = 線段二的終點 → 共用點為 (x1,y1) */
            bx = x1; by = y1;
            ax = x2; ay = y2;
            cx = x3; cy = y3;
        } else if (x2 == x3 && y2 == y3) 
        {
            /* 線段一的終點 = 線段二的起點 → 共用點為 (x2,y2) */
            bx = x2; by = y2;
            ax = x1; ay = y1;
            cx = x4; cy = y4;
        } else 
        {
            /* 線段一的終點 = 線段二的終點 → 共用點為 (x2,y2) */
            bx = x2; by = y2;
            ax = x1; ay = y1;
            cx = x3; cy = y3;
        }

        /* 第四點 D = A + C - B，分別對 x、y 座標計算 */
        printf("%.3f %.3f\n", ax + cx - bx, ay + cy - by);
    }
    return 0;
}