#include <stdio.h>
#include <stdlib.h>

/*
 * 遞迴分解數字，按孟加拉計數單位輸出
 * kuti (千萬), lakh (十萬), hajar (千), shata (百), 然後是個位數
 */
void bangla(long long n) {
    /* 千萬位（10,000,000） */
    if (n >= 10000000) { // kuti
        bangla(n / 10000000);
        printf("kuti ");
        n %= 10000000;
    }

    /* 十萬位（100,000） */
    if (n >= 100000) {    // lakh
        bangla(n / 100000);
        printf("lakh ");
        n %= 100000;
    }

    /* 千位（1,000） */
    if (n >= 1000) {      // hajar
        bangla(n / 1000);
        printf("hajar ");
        n %= 1000;
    }

    /* 百位（100） */
    if (n >= 100) {       // shata
        bangla(n / 100);
        printf("shata ");
        n %= 100;
    }

    /* 個位數（包含十位） */
    if (n > 0) {          // 剩餘的個位數
        printf("%lld ", n);
    }
}

/*
目標：
讀入十進位整數，按孟加拉計數系統分解並輸出。
孟加拉數字系統以特定單位區分：kuti (千萬), lakh (十萬), hajar (千), shata (百)。

解法：
1. 使用遞迴函式 bangla() 逐級分解數字。
2. 從最高位（千萬）開始，依序檢查並輸出各級單位名稱。
3. 每逐一層級後，用模運算取出餘數，繼續處理更低位。
4. 最後剩餘的 < 100 的數直接輸出。
5. 對每個輸入，依序號格式輸出結果。

解法：
1. 使用遞迴函式逐級分解，各層級獨立判斷。
2. 從千萬位開始，依序輸出各單位名稱與商數。
3. 每層用模運算取出餘數，再進行下一級計算。

技巧：
1. 遞迴寫法讓每級單位邏輯重複清楚，易於理解與維護。
2. 使用 long long 儲存，可應付大整數輸入。
3. 每層判斷用獨立 if，可避免冗長的 if-else 結構，且邏輯清晰。

隱藏的小細節：
1. 孟加拉計數系統並非標準十進位分組（不是按千位分），
   而是用特定的單位階梯：千萬 > 十萬 > 千 > 百。
2. 各層次間單位數量不均勻，但程式直接用除法處理無需轉換。
3. 若輸入 0，需特判輸出 "0"，避免遞迴函式輸出空字串。
4. 最後輸出含空白，該空白在換行前自然打印，題目通常接受此格式。
*/
int main()
{
    /* 輸入的數字 */
    long long int number;
    /* 編號計數（從 1 開始） */
    int time = 1;

    /* 讀到 EOF 為止 */
    while(scanf("%lld", &number) != EOF)
    {
        /* 依題目格式輸出編號 */
        printf("%d. ", time);

        /* 特判 0 的情況 */
        if(number == 0)
        {
            printf("0");
        }
        else
        {
            /* 呼叫遞迴函式分解數字 */
            bangla(number);
        }
        printf("\n");
        time++;
    }
    return 0;
}